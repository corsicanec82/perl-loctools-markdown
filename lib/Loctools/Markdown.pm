=head1 NAME

Loctools::Markdown - Localization-focused Markdown parser/generator

=head1 DESCRIPTION

Loctools::Markdown is a zero-dependency Markdown format parser, focused on
building an AST representation of the document that is easy to localize
and then have it rendered back to a Markdown file while trying to preserve
as much original formatting as possible.

